export function getProduct(id) {
  return products.find(p => p.id === id)
}

export function getByCategory(slug) {
  return products.filter(p => p.category === slug)
}

export function searchProducts(query) {
  const q = query.toLowerCase().trim()
  if (!q) return products
  return products.filter(p =>
    p.title.toLowerCase().includes(q) ||
    p.category.toLowerCase().includes(q) ||
    p.description.toLowerCase().includes(q)
  )
}

export function getDeals() {
  return products.filter(p => p.mrp > p.price).sort((a, b) => {
    const discA = (a.mrp - a.price) / a.mrp
    const discB = (b.mrp - b.price) / b.mrp
    return discB - discA
  })
}

export function getRelated(product, limit = 6) {
  return products
    .filter(p => p.category === product.category && p.id !== product.id)
    .slice(0, limit)
}

export function filterProducts(params = {}) {
  let list = [...products]

  if (params.category) {
    list = list.filter(p => p.category === params.category)
  }
  if (params.search) {
    list = searchProducts(params.search)
  }
  if (params.deals === 'true' || params.deals === true) {
    list = getDeals()
  }
  if (params.minPrice) {
    list = list.filter(p => p.price >= Number(params.minPrice))
  }
  if (params.maxPrice) {
    list = list.filter(p => p.price <= Number(params.maxPrice))
  }

  const total = list.length
  const page = Math.max(1, Number(params.page) || 1)
  const limit = Number(params.limit) || total
  const start = (page - 1) * limit

  return {
    products: list.slice(start, start + limit),
    total,
    page,
    pages: Math.ceil(total / limit) || 1,
  }
}
